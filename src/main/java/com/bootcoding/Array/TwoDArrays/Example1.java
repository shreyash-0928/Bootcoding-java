package com.bootcoding.Array.TwoDArrays;

public class Example1 {
    public static void main(String[] args) {
        int a[][] = new int[3][4];
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 4; j++) {
                System.out.println(a[i][j]+"");
            }
            System.out.println();
        }
    }
}
